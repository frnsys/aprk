	<div class="intro">
		<h1>Groupthink</h1>
		<h3>A sociobehavioral theory describing how group organization can impair our thinking.</h3>

		<div class="cols cols-2">
			<p>Groups are an important social structure of people. They can give us avenues for socializing, a means of building our identity, or a purpose which we can strive for. But sometimes they can make us act in regrettable ways. Maybe you got caught up in a fad, that you look back on with embarrassment. Maybe you see some legislation passed, and can't help but wonder: "What were they thinking?".</p>
			<p>The things we do in groups can do more than just embarass us. They can make us act in atrocious ways. The rise of Nazism and the <a href="http://articles.latimes.com/2004/jul/10/nation/na-groupthink10">Bay of Pigs invasion</a> are some particularly troubling examples. Even lessons about groupthink <a href="http://en.wikipedia.org/wiki/The_Third_Wave">ended up succumbing to it</a>. What happened? What <em>were</em> they thinking?</p>
			<p>Groupthink theory tries to answer these questions. The theory, first described by <a href="http://en.wikipedia.org/wiki/Irving_Janis">Irving Janis</a> in 1972, lays out a set of symptoms indicating when a group's capacity for decision making diminishes. After you become familiar with them, you may start noticing them everywhere!</p>
		</div>
	</div><!-- intro -->

	<h2>The Symptoms</h2>
	<div class="cols cols-2">
		<p>There are three major interpretations of groupthink theory: <b>strict</b>, which claims that groupthink occurs only when all symptoms are present, <b>additive</b>, which claims that groupthink gets stronger as more symptoms are present, and <b>liberal</b> (or particularistic), which claims that the requisite symptoms indicating groupthink vary depending on the particular situation. </p>
		<p>In addition to the following symptoms, <b>Irving Janis</b> hypothesized that groupthink is more likely in groups that are "highly cohesive, insulated from experts, perform limited search and appraisal of information, operate under directed leadership, and experience conditions of high stress with low-self esteem and little hope of finding a better solution to a pressing problem than that favored by the leader or influential members."</p>
	</div>
